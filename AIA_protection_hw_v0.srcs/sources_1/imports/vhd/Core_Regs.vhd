-- Description:
--   Módulo responsável pelo armazenamento de registros de configuração e 
--   controle dos componentes internos do FPGA.
--   Realiza a interface entre o processador (via GPIOs) 
--   e os blocos lógicos internos do projeto.
--
--   Principais funções:
--     - Disponibiliza 256 portas de entrada (in_Port_000 .. in_Port_255) 
--       para leitura pelo processador.
--     - Disponibiliza 256 portas de saída (out_Port_000 .. out_Port_255) 
--       configuráveis pelo processador.
--     - Suporta operações de leitura e escrita através de sinais 
--       chipselect, read, write, address, writedata e readdata.
--     - Pode gerar interrupção via sinal "irq".
--
--   Aplicação típica:
--     Este bloco atua como um "core de registradores" (core_regs), permitindo 
--     que o processador configure, monitore e controle periféricos ou lógicas 
--     implementadas no FPGA de forma simples e escalável.
--------------------------------------------------------------------------------
-- Revisions  :
--   YYYY-MM-DD  Versão  Autor    Descrição
--   ----------  ------  -------  ----------------------------------------------
--   2025-08-27  1.0     André    Criação da primeira versão.
